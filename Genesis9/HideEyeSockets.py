import bpy
import bmesh

def printfaces():
    obj = bpy.context.edit_object
    if obj and obj.type == 'MESH' and bpy.context.mode == 'EDIT_MESH':
        bm = bmesh.from_edit_mesh(obj.data)
        selected_faces = [f for f in bm.faces if f.select]
        if selected_faces:
            print("[")
            idn: str = "    "
            for f in selected_faces:
                print(f"{idn}{f.index},")
            print("]")
        else:
            print("No face selected.")
    else:
        print("Please select a mesh and enter Edit Mode (Face Select).")

def process_object(obj, faces_to_select, vg_name):
    max_index = len(obj.data.polygons) - 1
    valid_faces = [i for i in faces_to_select if i <= max_index]

    if not valid_faces:
        print(f"No valid faces for object {obj.name}")
        return

    bpy.context.view_layer.objects.active = obj
    bpy.ops.object.mode_set(mode='EDIT')
    bm = bmesh.from_edit_mesh(obj.data)

    for f in bm.faces:
        if f.select and f.index not in valid_faces:
            f.select = False

    bmesh.update_edit_mesh(obj.data)
    bpy.ops.object.mode_set(mode='OBJECT')

    vg = obj.vertex_groups.new(name=vg_name)
    verts = set()
    for idx in valid_faces:
        verts.update(obj.data.polygons[idx].vertices)
    vg.add(list(verts), 1.0, 'ADD')

    mask_mod = obj.modifiers.new(name=f"hide{vg_name}", type='MASK')
    mask_mod.vertex_group = vg.name
    mask_mod.invert_vertex_group = True

def hideface(vertname, faces):
    for obj in bpy.context.selected_objects:
        if obj.type == 'MESH':
            process_object(obj, faces, vertname)

